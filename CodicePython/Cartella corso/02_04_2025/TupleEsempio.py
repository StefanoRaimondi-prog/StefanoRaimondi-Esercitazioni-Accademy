# Creiamo una tupla con alcune informazioni su una persona
persona = ("Luca", 30, "Milano")

# Accediamo agli elementi della tupla usando gli indici
nome = persona[0]       # primo elemento: "Luca"
eta = persona[1]        # secondo elemento: 30
citta = persona[2]      # terzo elemento: "Milano"

# Stampiamo le informazioni in una frase
print(f"{nome} ha {eta} anni e vive a {citta}.")
